"""
Python = Linguagem de progamção
Tipo de tipagem = Dinâmica / Forte
srt -> String -> Texto
Strings são textos que estão dentro de aspas
"""
print(1234) # 1234

# Aspas simples
print('Meu nome') # Meu nome
print(1, 'Meu "nome"') # Meu "nome"

# Aspas duplas
print("Meu nome") # Meu nome
print(2, "Meu 'nome'") # Meu 'nome'

# Escape
print("Meu \"nome\"") # Meu "nome"

# r
print(r"Meu \"nome\"") # Meu \"nome\"

# Ei!
print('Python')